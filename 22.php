 <h2>Sistemas de Informacion de Gestión de Relaciones con los Clientes (CRM)</h2>
 <h3>• Son sistemas orientados a mejorar la relación de los clientes, conseguir y mantenerlos. Desarrollar relaciones más fuertes.</h3>
 <h3>• Buscan hacer el buen trato a sus clientes el diferenciador con respecto a sus competidores. Ventaja competitiva. </h3>
 <h3>• En un entorno basado en web la relación con el cliente requiere que sea mas personal, para lo cual se obtiene estudiando: perfiles , hábitos de compra y estrategias de marketing.</h3>
 <h3>• Marketing personal: se crean campañas a sectores que esten mas propensos a contratarlos.</h3>
 <h3>• Los clientes fieles generan la mayor cantidad de ingresos en una empresa.</h3>
 <h3>• Clientes satisfechos generan nuevos clientes por recomendación.</h3>
 <h3>• Recuperar un cliente perdido es el doble de difícil de atrar que a uno totalmente nuevo.</h3>
 <h3>Componentes de un CRM</h3>
 <h3>1.- Servicio al cliente: 
• Centros de atención Telefónica (Call-center).
• Centros de ayuda (Help-Desk).
• Apps y aplicaciones Web para el apoyo de clientes.
• Feedback.</h3>
 <h3>2.- Automatización de ventas y marketing.
• Administración de clientes.
• Administración de campañas.
• Administración de ventas.
• Análisis de ventas.
• Selección de clientes de mayor potencial.
• Soporte para la configuración del producto.
• Entre otras.</h3>
 <h3>3.- Analítica e inteligencia del negocio.
• Es el empleo de herramientas y técnicas para la identificación de
oportunidades de negocio en base a los datos de los clientes actuales.
• Para ello se emplean cubos de datos o sistemas OLAP (On Line
Analytical Processing)</h3>
 <h3>4.- Integración.
• Es el conjunto de software que da soporte a un CRM conformado por
un Front Office y un Back Office.
• Front-Office es el subconjunto de software que tiene conexión directa
con el cliente, principalmente tomar pedidos, configurar productos y
dar soporte post-venta(al producto). Analizan y procesan
produciendo información del cliente casi de forma instantanea.
• Back-Office Es el subconjunto de herramientas que no tienen relación
o interacción directa con el cliente. Pero si almacenan y procesan los
datos adquiridos de él. Requiere de operaciones de analisis o Mineria
de datos para obtener onformación compleja del comportamiento de
clientes.</h3>
 <h3>Software CRM popular: 
• SAP
• Oracle Peoplesoft
• Salesforce.com
</h3>
